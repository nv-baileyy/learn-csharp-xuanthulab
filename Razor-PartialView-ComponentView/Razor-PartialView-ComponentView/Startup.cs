using Microsoft.AspNetCore.Builder;
using Microsoft.AspNetCore.Hosting;
using Microsoft.AspNetCore.HttpsPolicy;
using Microsoft.Extensions.Configuration;
using Microsoft.Extensions.DependencyInjection;
using Microsoft.Extensions.Hosting;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
/*
    PartialView:- file cshtml khong co @page
                - chia nhỏ page thanhf các file nhỏ
                - sử dụng lại các thành phần (tránh trùng lặp code)
    Component ~ Partical View ~ DI = Mini Razor Page
    phai co -> [ViewComponent] hoac la them hau to *ViewComponent, hoac la ke thua ViewComponent
